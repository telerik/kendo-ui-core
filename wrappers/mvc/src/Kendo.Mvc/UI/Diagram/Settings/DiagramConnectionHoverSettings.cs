namespace Kendo.Mvc.UI
{
    using System;
    using System.Linq;
    using System.Collections.Generic;
    using System.Web.Routing;
    using Kendo.Mvc.Extensions;

    public class DiagramConnectionHoverSettings : JsonObject
    {
        public DiagramConnectionHoverSettings()
        {
            //>> Initialization
        
            Stroke = new DiagramConnectionHoverStrokeSettings();
                
        //<< Initialization
        }

        //>> Fields
        
        public DiagramConnectionHoverStrokeSettings Stroke
        {
            get;
            set;
        }
        
        //<< Fields

        protected override void Serialize(IDictionary<string, object> json)
        {
            //>> Serialization
        
            var stroke = Stroke.ToJson();
            if (stroke.Any())
            {
                json["stroke"] = stroke;
            }
                
        //<< Serialization
        }
    }
}
